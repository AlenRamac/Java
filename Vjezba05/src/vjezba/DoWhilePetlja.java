package vjezba;

public class DoWhilePetlja {

	public static void main(String[] args) {
		
		// osigurava minimalno jednu iteraciju petlje
		// uvjet izlaska iz petlje je na kraju
		int i=10;
		do {
			System.out.println("a");
		}while(i>10);
		
		// sve ostalo što vrijedi za for i while, vrijedi i za do while
		
		//beskonačna do while petlja
		do {
			//break;
		}while(true);
		
	}
	
}
